package Learning_java;

public class Calculation {
	public double sum(double a, double b) {
		return a + b;
	}

	public double subtract(double a, double b) {
		return a - b;
	}

	public double multiple(double a, double b) {
		return a * b;
	}

	public double divide(double a, double b) {
		return a / b;
	}
}
